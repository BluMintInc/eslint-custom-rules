export const enforceVerbNounNaming = createRule<[], MessageIds>({
  name: 'enforce-verb-noun-naming',
  meta: {
    type: 'suggestion',
    docs: {
      description: 'Enforce verb phrases for functions and methods',
      recommended: 'error',
      requiresTypeChecking: false,
      extendsBaseRule: false,
    },
    schema: [],
    messages: {
      functionVerbPhrase:
        'Function "{{name}}" should start with an action verb followed by the thing it acts on. Verb-first names tell readers this symbol performs work instead of representing data, which keeps APIs predictable and prevents accidental misuse. Rename "{{name}}" to a verb-noun phrase such as "fetchUsers" or "processRequest".',
    },
  },
  defaultOptions: [],
  create(context) {
    function extractFirstWord(name: string) {
      const firstChar = name.charAt(0);
      const rest = name.slice(1);
      const words = rest.split(/(?=[A-Z])/);
      return firstChar + words[0];
    }

    function toPhrase(name: string) {
      return name.split(/(?=[A-Z])/).join(' ');
    }

    function getPossibleTags(sentence: string, index = 0) {
      const doc = nlp(sentence);
      const terms = doc.terms().json();

      if (
        terms.length < index ||
        !terms[index]?.terms?.length ||
        !terms[index]?.terms[0]?.tags
      )
        return [];

      const tags = terms[index].terms[0].tags;
      return tags;
    }

    function isVerbPhrase(name: string): boolean {
      const firstWord = extractFirstWord(name);
      const firstWordLower = firstWord.toLowerCase();

      // Check for prepositions first
      if (PREPOSITIONS.has(firstWordLower)) {
        return true;
      }

      // Check allowlist taxonomy with clear precedence: verbs > utility > domain
      if (ALLOWLIST.verbs.has(firstWordLower)) {
        return true;
      }
      if (ALLOWLIST.utility.has(firstWordLower)) {
        return true;
      }
      if (ALLOWLIST.domain.has(firstWordLower)) {
        return true;
      }

      // Fall back to NLP approach if not found in allowlist
      const withoutPrefixTags = getPossibleTags(toPhrase(name));

      // Check if either form is recognized as a verb
      const isVerb = withoutPrefixTags.includes('Verb');
      const isPreposition = withoutPrefixTags.includes('Preposition');
      const isConjunction = withoutPrefixTags.includes('Conjunction');

      // For non-prepositions/conjunctions, require verb form
      if (isPreposition || isConjunction) {
        return true;
      }

      return isVerb;
    }

    function isPascalCase(name: string): boolean {
      return /^[A-Z][a-zA-Z0-9]*$/.test(name);
    }

    function hasPropsParameter(
      node:
        | TSESTree.FunctionDeclaration
        | TSESTree.ArrowFunctionExpression
        | TSESTree.FunctionExpression,
    ): boolean {
      if (!node.params.length) return false;

      const firstParam = node.params[0];
      if (firstParam.type === AST_NODE_TYPES.ObjectPattern) {
        return true; // Destructured props
      }

      if (firstParam.type === AST_NODE_TYPES.Identifier) {
        const paramName = firstParam.name.toLowerCase();
        return paramName === 'props';
      }

      return false;
    }

    function isReactComponent(node: TSESTree.Node): boolean {
      if (
        node.type !== AST_NODE_TYPES.FunctionDeclaration &&
        node.type !== AST_NODE_TYPES.ArrowFunctionExpression &&
        node.type !== AST_NODE_TYPES.FunctionExpression
      ) {
        return false;
      }

      // Get the function name
      let functionName = '';
      if (node.type === AST_NODE_TYPES.FunctionDeclaration && node.id) {
        functionName = node.id.name;
      } else if (
        node.type === AST_NODE_TYPES.ArrowFunctionExpression ||
        node.type === AST_NODE_TYPES.FunctionExpression
      ) {
        const parent = node.parent;
        if (
          parent?.type === AST_NODE_TYPES.VariableDeclarator &&
          parent.id.type === AST_NODE_TYPES.Identifier
        ) {
          functionName = parent.id.name;
        }
      }

      const returnsJsx = ASTHelpers.returnsJSX(node.body, context);
      const hasProps = hasPropsParameter(node);
      const isUnmemoized =
        !!functionName && functionName.endsWith('Unmemoized');

      // Check for React type annotations
      const hasReactType = (() => {
        // Handle ArrowFunctionExpression: const Foo: React.FC = () => ...
        if (node.type === AST_NODE_TYPES.ArrowFunctionExpression) {
          const parent = node.parent;
          if (parent?.type === AST_NODE_TYPES.VariableDeclarator) {
            const id = parent.id;
            if (
              id.type === AST_NODE_TYPES.Identifier &&
              id.typeAnnotation?.type === AST_NODE_TYPES.TSTypeAnnotation
            ) {
              const typeText = context.sourceCode.getText(
                id.typeAnnotation.typeAnnotation,
              );
              if (
                /\bReact\.(FC|FunctionComponent)\b/.test(typeText) ||
                /\b(FC|FunctionComponent)\b/.test(typeText)
              ) {
                return true;
              }
            }
          }
        }

        // Handle FunctionDeclaration/FunctionExpression return type: function Foo(): React.JSX.Element { ... }
        if (
          node.type === AST_NODE_TYPES.FunctionDeclaration ||
          node.type === AST_NODE_TYPES.ArrowFunctionExpression ||
          node.type === AST_NODE_TYPES.FunctionExpression
        ) {
          if (node.returnType?.type === AST_NODE_TYPES.TSTypeAnnotation) {
            const typeText = context.sourceCode.getText(
              node.returnType.typeAnnotation,
            );
            return (
              /\bReact\.(FC|FunctionComponent|JSX\.Element|ReactElement)\b/.test(
                typeText,
              ) ||
              /\b(FC|FunctionComponent|JSX\.Element|ReactElement)\b/.test(
                typeText,
              )
            );
          }
        }
        return false;
      })();

      // If the function has a PascalCase name, it's very likely a React component.
      // In the BluMint codebase, we follow the convention that PascalCase functions
      // are either components or layout components.
      if (functionName && isPascalCase(functionName)) {
        const filename = context.getFilename();
        const isJsxFile = /\.(tsx|jsx)$/.test(filename);
        // Treat as component if it returns JSX or has a React type.
        // In JSX/TSX files, we're more lenient and treat any PascalCase as a component
        // to avoid false positives for components that don't return JSX directly (e.g. return null).
        // In .ts files, we require more evidence (returns JSX or React type) to avoid
        // false positives for classes or helper functions that might use PascalCase.
        if (isJsxFile || returnsJsx || hasReactType) {
          return true;
        }
      }

      // If we have explicit React type, still treat it as a component to avoid false naming violations.
      if (hasReactType) {
        return true;
      }

      return (hasProps && returnsJsx) || (isUnmemoized && returnsJsx);
    }
    return {
      FunctionDeclaration(node) {
        if (!node.id) return;

        if (isReactComponent(node)) {
          return;
        }

        if (!isVerbPhrase(node.id.name)) {
          context.report({
            node: node.id,
            messageId: 'functionVerbPhrase',
            data: { name: node.id.name },
          });
        }
      },

      VariableDeclarator(node) {
        if (node.id.type !== AST_NODE_TYPES.Identifier) return;

        // Only check if it's a function
        if (
          node.init?.type === AST_NODE_TYPES.ArrowFunctionExpression ||
          node.init?.type === AST_NODE_TYPES.FunctionExpression
        ) {
          if (isReactComponent(node.init)) {
            return;
          }

          if (!isVerbPhrase(node.id.name)) {
            context.report({
              node: node.id,
              messageId: 'functionVerbPhrase',
              data: { name: node.id.name },
            });
          }
        }
      },

      MethodDefinition(node) {
        if (node.key.type !== AST_NODE_TYPES.Identifier) return;

        // Skip getters since they represent properties and should use noun phrases
        if (node.kind === 'get') return;

        // Skip constructors since they are special class methods
        if (node.kind === 'constructor') return;

        if (!isVerbPhrase(node.key.name)) {
          context.report({
            node: node.key,
            messageId: 'functionVerbPhrase',
            data: { name: node.key.name },
          });
        }
      },
    };
  },
});
